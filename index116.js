function getHexaColor(color){
    if(color == "white"){
        console.log("#FFFFFF");
    }else if(color == "black"){
        console.log("#000000");
    }else if(color == "blue"){
        console.log("#0b24Fb");
    }else if(color == "green"){
        console.log("#0e7e12");
    }else if(color == "yellow"){
        console.log("#fffd38");
    }else if(color == "pink"){
        console.log("#fec1cc");
    }
}
getHexaColor("white");
getHexaColor("black");
getHexaColor("blue");
getHexaColor("green");
getHexaColor("pink");
getHexaColor("yellow");